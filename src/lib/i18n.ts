export type Locale = keyof typeof translations;

export const fallbackLocale: Locale = "en";

export function getTranslation(
  locale: Locale,
  key: keyof typeof translations["en"],
): string {
  const lang = translations[locale] as Record<string, string> | undefined;
  const fallback = translations[fallbackLocale] as Record<string, string>;
  return (lang && lang[String(key)]) || fallback[String(key)] || String(key);
}

